package com.org;

public class Human {
		private int age;
		private String name;
		private Heart heart;
		
		
		
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public Heart getHeart() {
			return heart;
		}
		public void setHeart(Heart heart) {
			this.heart = heart;
		}
		
		
}
